#!/bin/bash

# Define the service name (as defined in your docker-compose.yml)
SERVICE_NAME="trab-bd-frontend-1"

# Get the container ID of the running service
CONTAINER_ID=$(docker ps -qf "name=${SERVICE_NAME}")

# Check if the container is running
if [ -z "$CONTAINER_ID" ]; then
  echo "Error: No running container found for the service ${SERVICE_NAME}"
  exit 1
fi

# Copy the node_modules directory from the container to the local frontend directory
docker cp $CONTAINER_ID:/app/frontend/node_modules ./frontend/

# Provide feedback
echo "node_modules has been copied from the container (${CONTAINER_ID}) to your local ./frontend/node_modules directory."
